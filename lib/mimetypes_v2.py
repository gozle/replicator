mimetypes.add_type('video/x-flv', '.flv', strict=True)
mimetypes.add_type('video/mp2t', '.ts', strict=True)
mimetypes.add_type('application/x-iso9660-image', '.iso', strict=True)
mimetypes.add_type('application/vnd.rar', '.rar', strict=True)
